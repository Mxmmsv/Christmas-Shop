const slider = document.querySelector(".slider_row");
const sliderButton = document.querySelectorAll(".slider_button");
const sliderContainer = document.querySelector(".slider_container");
let currentPosition = 0;
let counter = 0;

function clickCount() {
  if (body.offsetWidth >= 768) {
    return 3;
  }

  if (body.offsetWidth < 768) {
    return 6;
  }
}

function nextPage() {
  if (!sliderButton[1].classList.contains("slider_button_active")) return;
  counter += 1;

  currentPosition -= (slider.offsetWidth - sliderContainer.offsetWidth) / clickCount();
  sliderButton[0].classList.add("slider_button_active");
  if (counter === clickCount()) {
    sliderButton[1].classList.toggle("slider_button_active");
  }

  slider.style.transform = `translateX(${currentPosition}px)`;
  slider.style.transition = "all, ease-in-out, 0.6s";
}

function previousPage() {
  if (!sliderButton[0].classList.contains("slider_button_active")) return;
  counter -= 1;

  currentPosition += (slider.offsetWidth - sliderContainer.offsetWidth) / clickCount();
  sliderButton[1].classList.add("slider_button_active");
  if (counter === 0) {
    sliderButton[0].classList.toggle("slider_button_active");
  }
  slider.style.transform = `translateX(${currentPosition}px)`;
  slider.style.transition = "all, ease-in-out, 0.6s";
}

if (window.location.pathname === "/mxmmsv.github.io/Christmas-Shop/pages/home/index.html") {
  sliderButton[0].addEventListener("click", previousPage);
  sliderButton[1].addEventListener("click", nextPage);
  window.addEventListener("resize", () => {
    currentPosition = 0;
    counter = 0;
    sliderButton[0].classList.remove("slider_button_active");
    sliderButton[1].classList.add("slider_button_active");
    slider.style.transform = `translateX(0px)`;
    clickCount();
  });

  const timerGap = document.querySelector(".timer_gap");
  const timerCounter = document.createElement("div");
  timerCounter.className = "timer_counter";
  timerGap.appendChild(timerCounter);

  for (let i = 0; i < timerData.length; i += 1) {
    const timer = document.createElement("div");

    const h2 = document.createElement("h2");
    h2.textContent = timerData[i].value;
    timer.appendChild(h2);

    const h4 = document.createElement("h4");
    h4.textContent = timerData[i].label;
    timer.appendChild(h4);

    timerCounter.appendChild(timer);

    if (i !== timerData.length - 1) {
      const separator = document.createElement("span");
      separator.className = "separator";
      timerCounter.appendChild(separator);
    }
  }

  function timerUpdater() {
    let today = new Date();
    let finalDot = new Date("January 1, 2026 00:00:00");
    let timer = finalDot - today;

    const day = Math.floor(timer / (1000 * 60 * 60 * 24));
    const hour = Math.floor((timer % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
    const minute = Math.floor((timer % (1000 * 60 * 60)) / (1000 * 60));
    const second = Math.floor((timer % (1000 * 60)) / 1000);

    timerData[0].value = day;
    timerData[1].value = hour;
    timerData[2].value = minute;
    timerData[3].value = second;

    const h2Elements = timerCounter.querySelectorAll("h2");
    for (let i = 0; i < timerData.length; i += 1) {
      h2Elements[i].textContent = timerData[i].value;
    }
  }
  setInterval(timerUpdater, 1000);
}

function randomCard(a) {
  if (window.location.pathname === "/mxmmsv.github.io/Christmas-Shop/pages/gifts/gifts.html") {
    if (a === 0) {
      a += 1;
      return 0;
    }
    for (let i = 0; i < gifts.length; i += 1) {
      return (a += i);
    }
  }
  return Math.floor(Math.random() * gifts.length);
}

function imgGenerator(n) {
  if (gifts[n].category === "For Work") {
    return "../../assets/img/gift-for-work.png";
  }

  if (gifts[n].category === "For Health") {
    return "../../assets/img/gift-for-health.png";
  }

  if (gifts[n].category === "For Harmony") {
    return "../../assets/img/gift-for-harmony.png";
  }
}

